package com.nexa.javabasics;

public class DatatypesDemo {

	public static void main(String[] args) {
		
		//Integer
		int num = 34;
		byte by = 127;
		short sh = 558;
		long l = 45678;
		
		//Float
		float f = 4.6f;
		double d = 5.8;
		
		//Character
		char c = 'k';
		char ch = '5';	//literals
		
		//Boolean
		boolean b = true;
		
	}

}
